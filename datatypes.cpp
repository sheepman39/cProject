#include <iostream>

using namespace std;

int main()
{
    // char stores a single letter
    // MUST be single quotes
    char grade = 'A';

    // string stores a string
    // MUST be double quotes
    string phrase = "This is the way";

    // int stores a positive or negative whole number
    int money = 50;

    // floats store less decimal points
    // use double as default
    float gpa = 4.3;

    // double stores more decimals than a float
    double pi = 3.14159265358979323846264338327950288419716;

    // booleans set a value as true or false
    // similar to most other languages
    bool isCoding = true;

    return 0;
}